//-----------------------------------------------------------------------------
#include "BakedGood.h"
//-----------------------------------------------------------------------------
#include <sstream>
#include <iomanip>
using namespace std;
//-----------------------------------------------------------------------------
BakedGood::BakedGood() : price(0.0)
{
}

//-----------------------------------------------------------------------------
BakedGood::BakedGood(double price) : price(price)
{
}
//-----------------------------------------------------------------------------
BakedGood::~BakedGood()
{
}
//-----------------------------------------------------------------------------
string BakedGood::toString() const
{
    ostringstream str;
    str << fixed << setprecision(2);
    str << " ($" << price << ")";
    return str.str();
}
